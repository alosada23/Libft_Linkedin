#include <libft.h>

char ft_clasif_printf(char c)
{
	if (c == 'c' || c == 's' || c == 'd' || c == 'i' || 
		c == 'u' || c == 'x' || c == 'p' || c == '%')
		return (c);
	return (0); // Si no es un formato v√°lido
}
